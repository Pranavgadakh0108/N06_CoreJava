//program to demonstrate on assignment operator
package com.tns.operator;

public class AssignmentOperatorDemo {

	public static void main(String[] args) {
		int x= 12, y=4;
		x-=y; //x=x-y
		System.out.println(x);
		x*=y; //x=x*y
		System.out.println(x);
		x/=y; //x=x/y
		System.out.println(x);
		x+=y; //x=x+y
		System.out.println(x);
		x%=y; //x=x%y
		System.out.println(x);

	}

}
