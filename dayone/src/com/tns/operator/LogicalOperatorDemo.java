//program to demonstrate on logical operator
package com.tns.operator;

public class LogicalOperatorDemo {

	@SuppressWarnings("unused")
	
	public static void main(String[] args) {
		System.out.println(41!=3 && 3>=5);
		System.out.println(41!=3 || 3>=5);
		System.out.println(!(41!=3 || 3>=5));

	}

}
